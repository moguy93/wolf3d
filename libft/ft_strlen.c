#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i + 1] == '\0' || s[i + 2] == '\0')
			return ((s[i + 1] == '\0') ? (i + 1) : (i + 2));
		else if (s[i + 3] == '\0' || s[i + 4] == '\0')
			return ((s[i + 3] == '\0') ? (i + 3) : (i + 4));
		else if (s[i + 5] == '\0' || s[i + 6] == '\0')
			return ((s[i + 5] == '\0') ? (i + 5) : (i + 6));
		else if (s[i + 7] == '\0' || s[i + 8] == '\0')
			return ((s[i + 7] == '\0') ? (i + 7) : (i + 8));
		else if (s[i + 9] == '\0' || s[i + 10] == '\0')
			return ((s[i + 9] == '\0') ? (i + 9) : (i + 10));
		else if (s[i + 11] == '\0' || s[i + 12] == '\0')
			return ((s[i + 11] == '\0') ? (i + 11) : (i + 12));
		else if (s[i + 13] == '\0' || s[i + 14] == '\0')
			return ((s[i + 13] == '\0') ? (i + 13) : (i + 14));
		else if (s[i + 15] == '\0' || s[i + 16] == '\0')
			return ((s[i + 15] == '\0') ? (i + 15) : (i + 16));
		i += 16;
	}
	return (0);
}
