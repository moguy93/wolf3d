#include "libft.h"

void	ft_bzero(void *s, size_t nb)
{
	char	*b;
	long	n;

	b = (char*)s;
	n = (long)nb - 1;
	while (n >= 0)
	{
		b[n] = '\0';
		if ((n - 1) >= 0)
			b[n - 1] = '\0';
		if ((n - 2) >= 0)
			b[n - 2] = '\0';
		if ((n - 3) >= 0)
			b[n - 3] = '\0';
		if ((n - 4) >= 0)
			b[n - 4] = '\0';
		if ((n - 5) >= 0)
			b[n - 5] = '\0';
		if ((n - 6) >= 0)
			b[n - 6] = '\0';
		if ((n - 7) >= 0)
			b[n - 7] = '\0';
		n -= 7;
	}
}
